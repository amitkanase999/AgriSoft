package com.Fertilizer.bean;

public class CreditProductReportBean {

	private String custname;
	private Double fertilizerBalance;
	private Double seedBalance;
	private Double pesticideBalancel;
	
	
	
	public String getCustname() {
		return custname;
	}
	public void setCustname(String custname) {
		this.custname = custname;
	}
	public Double getFertilizerBalance() {
		return fertilizerBalance;
	}
	public void setFertilizerBalance(Double fertilizerBalance) {
		this.fertilizerBalance = fertilizerBalance;
	}
	public Double getSeedBalance() {
		return seedBalance;
	}
	public void setSeedBalance(Double seedBalance) {
		this.seedBalance = seedBalance;
	}
	public Double getPesticideBalancel() {
		return pesticideBalancel;
	}
	public void setPesticideBalancel(Double pesticideBalancel) {
		this.pesticideBalancel = pesticideBalancel;
	}
	
	
	
}
